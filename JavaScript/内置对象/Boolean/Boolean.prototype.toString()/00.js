// Boolean.prototype.toString() 返回字符串形式
//     语法： booleanObject.toString()

let boolean1 = true
let boolean2 = false
let str = boolean1.toString()
console.log(boolean1.toString()); // true
console.log(boolean2.toString()); // false
console.log(typeof(boolean1)); // boolean
console.log(typeof(str)); // string